const makeOrder = (
  id: string,
  invNo: string,
  custId: string,
  storeNum: string,
  company: string,
  address: string,
  status: OrderStatus,
  daysAgo: number,
  items: Array<{
    productId: string;
    productName: string;
    unit: string;
    qty: number;
    rate: number;
  }>,
) => {
  const itemsWithAmount = items.map((i) => ({
    ...i,
    quantity: i.qty,
    amount: i.qty * i.rate,
  }));
  const total = itemsWithAmount.reduce((s, i) => s + i.amount, 0);
  const createdAt = BigInt(Date.now() - daysAgo * 86400000);
  const deliveredAt =
    status === OrderStatus.delivered
      ? BigInt(Date.now() - (daysAgo - 1) * 86400000)
      : undefined;
  return {
    id,
    invoiceNumber: invNo,
    customerId: custId,
    storeNumber: storeNum,
    companyName: company,
    address,
    status,
    totalAmount: total,
    items: itemsWithAmount.map((i) => ({
      productId: i.productId,
      productName: i.productName,
      unit: i.unit,
      quantity: i.quantity,
      rate: i.rate,
      amount: i.amount,
    })),
    createdAt,
    deliveredAt,
    isDeleted: false,
    deleteReason: "",
  };
};

