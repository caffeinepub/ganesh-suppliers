export function formatCurrency(amount: number): string {
  return new Intl.NumberFormat("en-IN", {
    style: "currency",
    currency: "INR",
    maximumFractionDigits: 0,
  }).format(amount);
}

export function formatDate(ts: bigint | number | undefined): string {
  if (!ts) return "-";
  const d = new Date(typeof ts === "bigint" ? Number(ts) : ts);
  return d.toLocaleDateString("en-IN", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
  });
}

export function formatDateTime(ts: bigint | number | undefined): string {
  if (!ts) return "-";
  const d = new Date(typeof ts === "bigint" ? Number(ts) : ts);
  return d.toLocaleString("en-IN", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}
